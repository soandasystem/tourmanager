// SetCursoRoutes creates curso routes
func SetCursoRoutes(ctx context.Context, cfg config.Config, r *gin.Engine, p ports.CursoService) {
	//	r.Use(middlewares.Recover())

	r.POST("/api/v3.5/curso", createCurso(p))
	r.GET("/api/v3.5/curso", getAllCurso(p))
	r.GET("/api/v3.5/curso/informe", getInfCurso(p))
	r.GET("/api/v3.5/curso/:id", getCursoByID(p))
	r.PATCH("/api/v3.5/curso/:id", updateCurso(p))
	r.DELETE("/api/v3.5/curso/:id", deleteCurso(p))
}

// @Summary Create curso
// @Description Creates a new curso
// @Tags colegion
// @Param user body models.CreatecursoReq true "New colegio to be created"
// @Success 201 {object} models.cursoResp "OK"
// @Failure 400 {object} object
// @Failure 408 {object} object
// @Failure 500 {object} object
// @Router /api/v3.5/curso [post]
func createCurso(p ports.CursoService) gin.HandlerFunc {
	return func(c *gin.Context) {
		// El ctx ya tiene timeout + schema
		ctx := c.Request.Context()

		// Leer el cuerpo de la solicitud
		body, err := io.ReadAll(c.Request.Body)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}

		// Parsear el JSON al modelo adecuado
		var curso models.CreateCursoReq
		err = json.Unmarshal(body, &curso)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}

		// Llamar al servicio para crear los curso
		id, err := p.Create(ctx, curso)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}

		// Devolver la respuesta con el código de estado adecuado
		result := util.NewMessageResponse("Registro Creado con id: "+id, id)

		response := util.NewSuccessResponse(result, http.StatusAccepted)
		c.JSON(response.StatusCode, response)
	}
}

// @Summary Get inf all curso
// @Description Gets all the curso
// @Tags curso
// @Success 200 {array} models.cursoInf "OK"
// @Failure 400 {object} object
// @Failure 401 {object} object
// @Failure 408 {object} object
// @Failure 500 {object} object
// @Router /api/v3.5/curso [get]
func getInfCurso(p ports.CursoService) gin.HandlerFunc {
	return func(c *gin.Context) {
		// El ctx ya tiene timeout + schema
		ctx := c.Request.Context()

		filter := make(map[string]interface{})

		// Obtener todos los parámetros de consulta
		queryParams := c.Request.URL.Query()
		// Convertir a un map[string]interface{}
		for key, values := range queryParams {
			if len(values) > 1 {
				// Si hay múltiples valores, los almacenamos como slice
				filter[key] = values
			} else {
				// Si hay un solo valor, lo almacenamos directamente
				filter[key] = values[0]
			}
		}
		result, err := p.GetInforme(ctx, filter)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}
		// Agregar el total count al header (fuera del wrapper JSON)
		c.Header("X-Total-Count", strconv.FormatInt(result.TotalCount, 10))

		// Enviar solo los items en el wrapper de éxito
		response := util.NewSuccessResponse(result.Items, http.StatusOK)
		c.JSON(response.StatusCode, response)
	}

}

