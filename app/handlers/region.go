// SetRegionRoutes creates region routes
func SetRegionRoutes(ctx context.Context, cfg config.Config, r *gin.Engine, p ports.RegionService) {
	//	r.Use(middlewares.Recover())

	r.POST("/api/v3.5/region", createRegion(p))
	r.GET("/api/v3.5/region", getAllRegion(p))
	r.GET("/api/v3.5/region/:id", getRegionByID(p))
	r.PATCH("/api/v3.5/region/:id", updateRegion(p))
	r.DELETE("/api/v3.5/region/:id", deleteRegion(p))
}

// @Summary Create region
// @Description Creates a new region
// @Tags region
// @Param user body models.CreateregionReq true "New region to be created"
// @Success 201 {object} models.regionResp "OK"
// @Failure 400 {object} object
// @Failure 408 {object} object
// @Failure 500 {object} object
// @Router /api/v3.5/region [post]
func createRegion(p ports.RegionService) gin.HandlerFunc {
	return func(c *gin.Context) {
		// El ctx ya tiene timeout + schema
		ctx := c.Request.Context()

		// Leer el cuerpo de la solicitud
		body, err := io.ReadAll(c.Request.Body)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}

		// Parsear el JSON al modelo adecuado
		var region models.CreateRegionReq
		err = json.Unmarshal(body, &region)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}

		// Llamar al servicio para crear los region
		id, err := p.Create(ctx, region)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}

		// Devolver la respuesta con el código de estado adecuado
		result := util.NewMessageResponse("Registro Creado con id: "+id, id)

		response := util.NewSuccessResponse(result, http.StatusAccepted)
		c.JSON(response.StatusCode, response)
	}
}

// @Summary Get all region
// @Description Gets all the region
// @Tags region
// @Success 200 {array} models.regionResp "OK"
// @Failure 400 {object} object
// @Failure 401 {object} object
// @Failure 408 {object} object
// @Failure 500 {object} object
// @Router /api/v3.5/region [get]
func getAllRegion(p ports.RegionService) gin.HandlerFunc {
	return func(c *gin.Context) {
		// El ctx ya tiene timeout + schema
		ctx := c.Request.Context()

		filter := make(map[string]interface{})
		result, err := p.GetAll(ctx, filter)
		if err != nil {
			response := util.NewErrorResponse(err, http.StatusInternalServerError)
			c.JSON(response.StatusCode, response)
			return
		}
		// Agregar el total count al header (fuera del wrapper JSON)
		c.Header("X-Total-Count", strconv.FormatInt(result.TotalCount, 10))

		// Enviar solo los items en el wrapper de éxito
		response := util.NewSuccessResponse(result.Items, http.StatusOK)
		c.JSON(response.StatusCode, response)
	}
}

